CREATE TABLE attendance (
    id INT AUTO_INCREMENT  PRIMARY KEY ,
    student_id INT NOT NULL,
    date DATE NOT NULL,
    status CHAR(1) NOT NULL CHECK (status IN ('P', 'A')),
    is_active BOOLEAN DEFAULT 1,
    
    -- Foreign key constraint referencing students table
    CONSTRAINT FK_attendance_student 
        FOREIGN KEY (student_id) REFERENCES students(id),
    
    -- Ensure one attendance record per student per date
    CONSTRAINT UK_attendance_student_date UNIQUE (student_id, date)
);

